package decisionmaking;

public class switchh {

	public static void main(String[] args) 
	{
		String favclr="black";
		switch(favclr) {
		case "purple":
		System.out.println("your fav color is purple");
		break;
		case "beige":
		System.out.println("your fav color is beige");
		break;
		case "black":
		System.out.println("your fav color is black");
		break;
		default:
		System.out.println("your fav color is neither red,green nor black");
		}

	}

}
